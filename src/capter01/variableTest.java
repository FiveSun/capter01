package capter01;

public class variableTest {
	public static void main(String[] args) {
		float a = 7f;
		float b = 3f;

		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(-a);
		System.out.println(++a);
		System.out.println(--b); 	
		a = 7f;
		System.out.println(a++);
		System.out.println(b--);

	}

}
